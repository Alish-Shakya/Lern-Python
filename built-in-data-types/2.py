# Write a program to convert float into integer and integer into float  



float_num = float(input("Enter a float number: "))
int_num = int(input("Enter an integer number: "))

# Conversions
float_to_int = int(float_num)
int_to_float = float(int_num)

# Display results
print("Float to Integer:", float_to_int)
print("Integer to Float:", int_to_float)


