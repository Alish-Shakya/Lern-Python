# program to demonstrate Boolean values and boolean expressioon


# Boolean values
a = True
b = False

print("Boolean values:")
print("a =", a)
print("b =", b)

# Boolean expressions
x = 10
y = 5

print("\nBoolean expressions:")
print("x > y :", x > y)
print("x < y :", x < y)
print("x == y :", x == y)
print("x != y :", x != y)

# Logical operators
print("\nLogical operations:")
print("a and b :", a and b)
print("a or b :", a or b)
print("not a :", not a)
