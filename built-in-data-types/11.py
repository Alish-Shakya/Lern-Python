# program to create a list and demonstrate indexing and slicing  

# Create a list
numbers = [10, 20, 30, 40, 50, 60, 70]

# Indexing
print("List:", numbers)
print("First element:", numbers[0])
print("Third element:", numbers[2])
print("Last element:", numbers[-1])

# Slicing
print("\nSlicing examples:")
print("First three elements:", numbers[0:3])
print("From index 2 to 5:", numbers[2:6])
print("Elements from index 3 to end:", numbers[3:])
print("Last three elements:", numbers[-3:])
