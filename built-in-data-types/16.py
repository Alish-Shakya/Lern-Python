# program to copy a list using assignment copy() and method slicing


# Create a list
numbers = [10, 20, 30, 40, 50]

# Using for loop
print("Using for loop:")
for num in numbers:
    print(num)

# Using while loop
print("\nUsing while loop:")
i = 0
while i < len(numbers):
    print(numbers[i])
    i += 1
